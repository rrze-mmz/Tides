    ]);
    $alice = User::factory()->create([
        'first_name' => 'Alice',
        'last_name' => 'Tester',
        'username' => 'alice01',
    ]);
    $gregor = User::factory()->create([
        'first_name' => 'Gregor',
        'last_name' => 'Tester',
        'username' => 'gregor01',
    ]);

    Livewire::test(UserDataTable::class)
        ->call('sortBy', 'username')
        ->assertSeeInOrder([$alice->username, $bob->username, $gregor->username]);
