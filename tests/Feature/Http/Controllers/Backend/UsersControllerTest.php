it('updates clip supervisor id if a portal admin updates a clip', function () {
    $clip = ClipFactory::ownedBy(signInRole(Role::MODERATOR))->create();

    expect($clip->supervisor_id)->toBeNull();

    auth()->logout();
    signInRole(EnumRole::ADMIN);
    patch(route('clips.edit', $clip), [
        'episode' => '1',
        'title' => 'changed',
        'description' => 'changed',
        'recording_date' => now(),
        'organization_id' => '1',
        'language_id' => '1',
        'context_id' => '1',
        'format_id' => '1',
        'type_id' => '1',
        'semester_id' => '1',
    ]);
    $clip->refresh();

    expect($clip->supervisor_id)->not->toBeNull();
});

