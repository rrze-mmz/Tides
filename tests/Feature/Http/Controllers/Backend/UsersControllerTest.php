test('admin user can update user information', function () {
    $user = User::factory()->create();
    patch(route('users.update', $user), [
        'first_name' => 'John',
        'last_name' => 'Doe',
        'email' => $user->email,
    ]);

    $user->refresh();

    assertDatabaseHas('users', [
        'first_name' => 'John',
        'last_name' => 'Doe',
        'email' => $user->email,
    ]);
});

